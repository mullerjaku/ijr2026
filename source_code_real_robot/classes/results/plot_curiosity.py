paths = [data[:PATH_LEN], data[PATH_LEN:2*PATH_LEN], data[2*PATH_LEN:]]

colors = ['#378ADD', '#1D9E75']#, '#D85A30']
labels = ['Exploration 1', 'Exploration 2']#, 'Exploration 3']

import matplotlib.cm as cm
import matplotlib.colors as mcolors
from matplotlib.lines import Line2D

cmaps = ['Blues', 'Greens']#, 'Oranges']

fig = plt.figure(figsize=(17, 6))

for i, (path, cmap_name, label) in enumerate(zip(paths, cmaps, labels)):
    ax = fig.add_subplot(1, 2, i + 1, projection='3d')

    x, y, z, flag = path[:, 0], path[:, 1], path[:, 2], path[:, 3]
    t = np.arange(len(path))

    cmap = cm.get_cmap(cmap_name)
    norm = mcolors.Normalize(vmin=0, vmax=len(path) - 1)

    sc = ax.scatter(x, y, z, c=t, cmap=cmap, norm=norm, s=18, alpha=0.85, depthshade=True)

    mask = flag == 1
    if mask.any():
        ax.scatter(
            x[mask], y[mask], z[mask],
            facecolors='white', edgecolors=cmap(0.85),
            s=90, linewidths=2, zorder=8, label='Goal/s Explored',
        )
        ax.legend(fontsize=12, loc='upper left')

    cb = fig.colorbar(cm.ScalarMappable(norm=norm, cmap=cmap), ax=ax,
                      shrink=0.6, pad=0.1, aspect=20)
    cb.set_ticks([0, len(path) - 1])
    cb.set_ticklabels(['0', '100 steps'], fontsize=14)

    ax.set_title(label, pad=8, fontsize=14)
    ax.set_xlabel('Distance Object 1', labelpad=6, fontsize=14)
    ax.set_ylabel('Distance Object 2', labelpad=6, fontsize=14)
    ax.set_zlabel('Distance Wall', labelpad=6, fontsize=14)
    ax.tick_params(labelsize=12)
    ax.view_init(elev=20, azim=45)
    # rozsah os
    ax.set_xlim(0, 1)
    ax.set_ylim(0, 1)
    ax.set_zlim(0, 1)

    # ticks po 0.2
    ticks = np.arange(0, 1.1, 0.2)
    ax.set_xticks(ticks)
    ax.set_yticks(ticks)
    ax.set_zticks(ticks)

