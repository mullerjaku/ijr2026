# ── Parse ────────────────────────────────────────────────────────────────────
# Odstraněno načítání bodů (pts), protože už nejsou potřeba pro výpočet vzdálenosti
def parse(raw):
    nov  = np.array([r[0] for r in raw])
    frus = np.array([r[1] for r in raw])
    entr = np.array([r[2] for r in raw])
    return nov, frus, entr

