def style_ax(ax, title, ylabel=None):
    ax.set_title(title, pad=8)
    ax.set_xlabel('Epoch', labelpad=4)
    if ylabel:
        ax.set_ylabel(ylabel, labelpad=6)
    ax.set_xlim(0, 95)
    ax.set_ylim(-0.12, 1.05)
