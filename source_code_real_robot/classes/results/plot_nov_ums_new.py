# ── Parse & Compute ───────────────────────────────────────────────────────────
def parse(raw):
    nov  = np.array([r[0] for r in raw])
    frus = np.array([r[1] for r in raw])
    entr = np.array([r[2] for r in raw])
    return nov, frus, entr

def compute(nov, frus, entr, window=5):
    n = len(nov)
    w_nov = np.zeros(n)
    w_um  = np.zeros(n)
    
    hist = []
    curr_w_nov = 0.3
    curr_w_um  = 0.7

    for i in range(n):
        hist.append(frus[i])
        if len(hist) > window:
            hist.pop(0)

        if len(hist) == window:
            diffs = np.diff(hist)
            if np.all(diffs > 0):
                curr_w_nov = 0.7
                curr_w_um  = 0.3
            elif np.all(diffs < 0):
                curr_w_nov = 0.3
                curr_w_um  = 0.7

        w_nov[i] = curr_w_nov
        w_um[i]  = curr_w_um

    pred = w_nov * nov + w_um * entr
    return w_nov, w_um, pred

